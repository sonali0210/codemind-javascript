console.log("====== Primitive Data Type : Number ====== " );

var age = 22;   
console.log("Age: ",age);
var typeOfAge = typeof(age);
console.log("Type of Age: ",typeOfAge);

var weight = 70.50;
console.log("Weight: ",weight);
var typeOfWeight = typeof(weight);
console.log("Type of Weight: ",typeOfWeight);

console.log("====== Primitive Data Type : String ====== ");

var city = "Pune";
console.log("City: ",city);
var typeOfCity = typeof(city);
console.log("Type of City: ",typeOfCity);

var pin = "411015";
console.log("Pin: ",pin);
var typeOfPin = typeof(pin);
console.log("Type of Pin: ",typeOfPin);

console.log("====== Primitive Data Type : Boolean ====== ");

var isMarried = true;
console.log("Is Married: ",isMarried);
var typeOfIsMarried = typeof(isMarried);
console.log("Type of IsMarried: ",typeOfIsMarried);

